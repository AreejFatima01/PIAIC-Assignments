// Program 2: Convert Fahrenheit to Celsius
// This program converts a temperature in Fahrenheit to Celsius.
const fahrenheitTemp: number = 68;
const celsiusTemp: number = (fahrenheitTemp - 32) * (5 / 9);

console.log(`The temperature in Celsius is approximately ${celsiusTemp.toFixed(2)}°C.`)

